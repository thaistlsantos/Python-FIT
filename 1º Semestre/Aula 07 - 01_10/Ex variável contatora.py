soma = 0

x = 0                   # inicia a variável de controle
while x <= 10:          # expressão que envolve a váriavel de  controle

    # faço alguma coisa com o valor atual
    soma = soma + x 
    print('Número Atual: {}, soma até agora: {}'
          .format(x, soma))
    
    x = x + 1           # alterar a várial de controle

